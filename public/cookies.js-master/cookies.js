// updateUserDisplay()
// updates the login/logout displays at top of page

// might be best to stringify the entire JSON recipe: 
// setRecipe(recipe)
// sets the recipe cookie to recipe

// getRecipe()
// gets the recipe in the recipe cookie

// getSearch()
// gets the value from the search cookie

// setSearch(search)
// sets the search cookie to search

// logout()
// logs out the user (deletes the cookie)

// login(username)
// sets the username cookie to username

// signedIn()
// returns true if someone is logged in and false otherwise


var docCookies = {
  updateUserDisplay: function () {
    if (this.signedIn()) {
        $("li.logout-btn").addClass("showBtn");
        $("li.logout-btn").removeClass("hideBtn");
        $("li.user-btns").addClass("hideBtn");
        $("li.user-btns").removeClass("showBtn");
    }
    else {
        $("li.logout-btn").addClass("hideBtn");
        $("li.logout-btn").removeClass("showBtn");
        $("li.user-btns").addClass("showBtn"); 
        $("li.user-btns").removeClass("hideBtn");         
    }
  },
  getRecipe: function () {
    return this.getItem("recipe");
  },
  setRecipe: function (recipe) {
    this.setItem("recipe", recipe);
  },
  getSearch: function () {
    return this.getItem("search");
  },
  setSearch: function (search) {
    this.setItem("search", search);
  },
  getUsername: function() {
    return this.getItem("username");
  },
  logout: function () { 
    this.removeItem("username");
    this.updateUserDisplay();
  },
  login: function (username) { 
    this.setItem("username", username);
    this.updateUserDisplay();
  },
  signedIn: function () { 
    return this.hasItem("username");
  },
  getItem: function (sKey) {
    if (!sKey) { return null; }
    return decodeURIComponent(document.cookie.replace(new RegExp("(?:(?:^|.*;)\\s*" + encodeURIComponent(sKey).replace(/[\-\.\+\*]/g, "\\$&") + "\\s*\\=\\s*([^;]*).*$)|^.*$"), "$1")) || null;
  },
  setItem: function (sKey, sValue, vEnd, sPath, sDomain, bSecure) {
    if (!sKey || /^(?:expires|max\-age|path|domain|secure)$/i.test(sKey)) { return false; }
    var sExpires = "";
    if (vEnd) {
      switch (vEnd.constructor) {
        case Number:
          sExpires = vEnd === Infinity ? "; expires=Fri, 31 Dec 9999 23:59:59 GMT" : "; max-age=" + vEnd;
          /*
          Note: Despite officially defined in RFC 6265, the use of `max-age` is not compatible with any
          version of Internet Explorer, Edge and some mobile browsers. Therefore passing a number to
          the end parameter might not work as expected. A possible solution might be to convert the the
          relative time to an absolute time. For instance, replacing the previous line with:
          */
          /*
          sExpires = vEnd === Infinity ? "; expires=Fri, 31 Dec 9999 23:59:59 GMT" : "; expires=" + (new Date(vEnd * 1e3 + Date.now())).toUTCString();
          */
          break;
        case String:
          sExpires = "; expires=" + vEnd;
          break;
        case Date:
          sExpires = "; expires=" + vEnd.toUTCString();
          break;
      }
    }
    document.cookie = encodeURIComponent(sKey) + "=" + encodeURIComponent(sValue) + sExpires + (sDomain ? "; domain=" + sDomain : "") + (sPath ? "; path=" + sPath : "") + (bSecure ? "; secure" : "");
    return true;
  },
  removeItem: function (sKey, sPath, sDomain) {
    if (!this.hasItem(sKey)) { return false; }
    document.cookie = encodeURIComponent(sKey) + "=; expires=Thu, 01 Jan 1970 00:00:00 GMT" + (sDomain ? "; domain=" + sDomain : "") + (sPath ? "; path=" + sPath : "");
    return true;
  },
  hasItem: function (sKey) {
    if (!sKey || /^(?:expires|max\-age|path|domain|secure)$/i.test(sKey)) { return false; }
    return (new RegExp("(?:^|;\\s*)" + encodeURIComponent(sKey).replace(/[\-\.\+\*]/g, "\\$&") + "\\s*\\=")).test(document.cookie);
  },
  keys: function () {
    var aKeys = document.cookie.replace(/((?:^|\s*;)[^\=]+)(?=;|$)|^\s*|\s*(?:\=[^;]*)?(?:\1|$)/g, "").split(/\s*(?:\=[^;]*)?;\s*/);
    for (var nLen = aKeys.length, nIdx = 0; nIdx < nLen; nIdx++) { aKeys[nIdx] = decodeURIComponent(aKeys[nIdx]); }
    return aKeys;
  }
};
